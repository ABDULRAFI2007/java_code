public class askcode2 {
    public static void main(String[] args) {
        
        for(int i=1;i<=4;i+=1){
            for(int r=1;r<=i;r+=1){
                System.out.print(r);
            }
            System.out.println();
        }
        for(int j =4;j>=1;j-=1){
            for (int q=1;q<=j;q+=1){
                System.out.print(q);
            }
            System.out.println();
        }
    }
    
}
